#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	size;
	int		i;
	char	*new;

	size = ft_strlen(s);
	new = (char *)malloc(size + 1);
	i = 0;
	while (s[i])
	{
		new[i] = f(s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
