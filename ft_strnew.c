#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;

	s = (char *)malloc(size + 1);
	if (s == NULL)
		return (NULL);
	s[size + 1] = '\0';
	while (size)
		s[size--] = '\0';
	return (s);
}
